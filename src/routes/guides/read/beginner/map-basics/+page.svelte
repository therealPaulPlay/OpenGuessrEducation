<p class="mb-4">
	Many advanced players prefer community maps, as they often offer a <b>more varied distribution</b>
	compared to official maps. Many official maps over-represent countries or areas that have more street view coverage (meaning,
	more panorama images), which makes them less interesting to play.<br /><br />Additionally,
	<b>community maps</b>
	often include a lot
	<b>more locations</b> than their official counterparts. However, in OpenGuessr, the <b>standard map</b> gets
	<b>regenerated constantly</b> to feature a near infinite amount of places.
</p>

