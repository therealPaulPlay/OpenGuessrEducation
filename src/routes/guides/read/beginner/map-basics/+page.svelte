<h2 class="text-2xl font-bold mt-8 mb-2">General Terms</h2>

<p class="mb-4">
    Let's now take a look at a few terms that are often being used by players
    when talking about maps:
</p>

<ol class="mb-2 list-disc list-inside">
    <li class="mb-2">
        <b>Pinpointability:</b> Pinpointable means, that locations are easy to precisely
        locate on the map. This is possible, when a distinct feature can be identified,
        such as a road sign or a famous building.
    </li>
    <li class="mb-2">
        <b>Distribution:</b> Terms such as country size, coverage density and landscape
        diversity are often being used when describing maps. Balanced usually means
        that players are as likely to be dropped in a small country and/or a country
        with litle coverag as being presented with a panorama from a large nation.
    </li>
    <li class="mb-2">
        <b>Handpicked / Arbitrarily generated:</b> While some maps feature handpicked
        locations, others were created using computer algorithms, that are sometimes
        falsely being referred to as "AI generated".
    </li>
    <li class="mb-2">
        <b>Official / Unofficial coverage:</b> These terms refer to street view,
        which has both images captured by Google (= official coverage), as well as
        images that were taken by users of the platform.
    </li>
</ol>
