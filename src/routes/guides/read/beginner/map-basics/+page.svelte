<h1 class="text-4xl font-bold mb-4">Map Basics</h1>
<p class="mb-2">
	A map refers to a <b>set of locations</b> that will be chosen
	<b>at random</b> when you play - well, it's not quite random, as the game keeps track of your previous locations to prevent
	repeats. Even game modes that do not expicitly name a map that they use, get their locations from a map. Let's take a look
	at a few specifics.
</p>

<h2 class="text-2xl font-bold mt-8 mb-2">Official vs. Community Maps</h2>
<p class="mb-2">
	In all three major guessing games, user created maps are available and known as community maps. Moreover, they include
	pages that showcase these maps.
</p>
