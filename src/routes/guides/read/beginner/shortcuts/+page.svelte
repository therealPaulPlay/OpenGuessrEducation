	</li>
	<li>
		Use <kbd class="kbd kbd-sm ml-2">W</kbd>, <kbd class="kbd kbd-sm ml-2">A</kbd>, <kbd class="kbd kbd-sm mx-2">S</kbd>
		and <kbd class="kbd kbd-sm mx-2">D</kbd> to move
	</li>
</ol>
