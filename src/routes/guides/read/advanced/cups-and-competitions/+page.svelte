<h1 class="text-4xl font-bold mb-4">Cups and Competitions</h1>
<p class="mb-2">
	At some point in your Geo-Guessing journey you might <b>feel ready</b> to put your skills to test. In this guide, we
	will find out how you can <b>compete</b> against other professional players.
</p>

<ArticleTip>The current plan for the GeoGuessr worldcup is not yet finalized and is subject to change.</ArticleTip>

<h2 class="text-2xl font-bold mt-8 mb-2">GeoGuessr World Cup</h2>
<p class="mb-2">
	To qualify for the GeoGuessr World Cup, you first have to be a <b>high-ranked</b> player in GeoGuessr to receive an
	invitation. Then, you will be able to play against other qualified players in various stages, such as the group stage.
	<b>Wildcard Events</b>
	can also help you secure a seat at the World Cup. The World Cup is an <b>in-person</b> event with a $50,000 prize pool.
</p>

<h2 class="text-2xl font-bold mt-8 mb-2">PlonkIt Ranked & Records</h2>
<p class="mb-2">
	On the PlonkIt website, you can compete against others in their own <b>ranked format</b>. There, the game modes are
	different compared to GeoGuessr's ranked Duels mode, and you will be matched up against other players by
	<b>community members</b> on their Discord Server. Moreover, you can also try to beat records on their leaderboards for
	GeoGuessr maps.
</p>

