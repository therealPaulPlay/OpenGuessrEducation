<h2 class="text-2xl font-bold mt-8 mb-2">OpenGuessr Competitions</h2>
<p class="mb-2">
	On OpenGuessr, Competitions are the best way to compete with other players. You can either participate in the <b
		>daily competition</b
	>, other <b>popular user-submitted competitions</b>, or create your own private ones to see how you stack up against
	professional players that <b>you can send the link to</b>. This makes hosting your own tournaments easy.
</p>
