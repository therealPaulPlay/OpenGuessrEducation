<h2 class="text-2xl font-bold mt-8 mb-2">Japanese Kana and Kanji</h2>
<p class="mb-2">
    <b>Japan</b> uses a combination of <b>kanji</b> characters (borrowed from Chinese) and two syllabic scripts, <b>hiragana</b> and <b>katakana</b>. 
    Recognizable kanji include characters like <b>日</b> (sun/day) or <b>山</b> (mountain). Hiragana and katakana are syllabaries 
    with simpler characters like <b>あ</b> or <b>カ</b>.
</p>
<p class="mb-4">
    A typical sentence might be: <b>こんにちは、お元気ですか？</b> (Hello, how are you?), blending kanji and kana.
</p>

