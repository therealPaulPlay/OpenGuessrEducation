<h2 class="text-2xl font-bold mt-8 mb-2">Japanese Kana and Kanji</h2>
<p class="mb-2">
	<b>Japan</b> uses a combination of <b>kanji</b> characters (borrowed from Chinese) and two syllabic scripts,
	<b>hiragana</b>
	and <b>katakana</b>. Recognizable kanji include characters like <b>日</b> (sun/day) or <b>山</b> (mountain). Hiragana
	and katakana are syllabaries with simpler characters like <b>あ</b> or <b>カ</b>.
</p>
<p class="mb-4">
	A typical sentence might be: <b>こんにちは、お元気ですか？</b> (Hello, how are you?), blending kanji and kana.
</p>

<Map region="Kanji Alphabet" interactive="true" showLabels="true" minLabelZoom="3" />

<p class="text-center mt-2">Countries that use the Japanese (Kana / Kanji) alphabet.</p>

<h2 class="text-2xl font-bold mt-8 mb-2">Devanagari Alphabet</h2>
<p class="mb-2">
	The <b>Devanagari alphabet</b> is used in <b>India and Nepal</b>, for languages like <b>Hindi</b> and <b>Nepali</b>.
	It's notable for its horizontal line running above the letters and includes characters like <b>अ, ब, म</b>.
</p>
<p class="mb-2">
	Example sentence: <b>नमस्ते, आप कैसे हैं?</b> (Hello, how are you?), makes it easy to identify Devanagari.
</p>

<ArticleTip>
	Look for the horizontal line across the top of letters to recognize scripts from India and Nepal.
</ArticleTip>

