<h2 class="text-2xl font-bold mt-8 mb-2">Strategies</h2>
<ul class="mb-2 list-disc list-inside">
	<li>Water heaters on <b>roofs</b>, very common and almost on every single house.</li>
	<li>Two-storey houses, often <b>unfinished</b> or <b>run-down</b>.</li>
	<li>Bollards with a <b>black top</b> and <b>red and grey reflectors</b>, white bottom; less common than in Italy.</li>
	<li>
		<b>Rifts at the top cameraview</b> due to bad image cropping, similar to <b>Montenegro</b> and rarely other countries.
