<p class="mb-2">
	<b>Austria</b> is a <b>landlocked country</b> in <b>Central Europe</b> known for its <b>imperial history</b> and
	<b>alpine landscapes</b>. It shares borders with <b>eight countries</b> and features diverse geography, from the towering 
	Alps to rolling hills and plains. Austria is famous for its cultural heritage, classical music, and picturesque 
	villages.
</p>

<h2 class="text-2xl font-bold mt-8 mb-2">Common places</h2>
<p class="mb-2">
	These are some of the most <b>famous locations</b> you may encounter in Austria:
</p>
<ul class="mb-2 list-disc list-inside">
	<li>
		<b>Vienna:</b> Capital city with <b>23 numbered districts</b>, <b>historical architecture</b>, and
		<b>extensive public transport</b>. Many streets are named after after <b>famous musicians or historical figures</b>.
	</li>
	<li><b>Salzburg:</b> <b>UNESCO Old Town</b> with <b>Baroque architecture</b> and <b>Mozart's birthplace</b>.</li>
	<li><b>Innsbruck:</b> <b>Alpine city</b> known for <b>winter sports</b> and <b>Habsburg landmarks</b>.</li>
	<li><b>Hallstatt:</b> Picturesque <b>lakeside village</b> with <b>salt mines</b> and <b>Alpine scenery</b>.</li>
	<li><b>Graz:</b> Second-largest city with <b>Renaissance architecture</b> and <b>modern arts scene</b>.</li>
	<li>
		<b>Wachau Valley:</b> <b>UNESCO site</b> along the <b>Danube</b> with <b>vineyards</b> and <b>medieval castles</b>.
	</li>
</ul>

