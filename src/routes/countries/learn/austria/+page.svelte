<h2 class="text-2xl font-bold mb-2 mt-8 mb-2">Road and Traffic Signs</h2>
<p class="mb-2">
	Some austrian road signs and traffic signs are unique and can help you identify the country you are in. Austria uses
	the <b>german alphabet</b>, which has the letters <b>ä, ö, ü and ß</b>, and is used in Germany, Austria and
	Switzerland. The letter <b>ß</b> is only used in the german alphabet and is a ligature of <b>ss</b>. The german word
	for <b>street is "Straße"</b> and can be found in Germany and Austria, Switzerland uses "Strasse".
</p>
<ul class="mb-2 list-disc list-inside">
	<li>
		<b>District numbering:</b> In <b>Vienna</b>, every street sign includes <b>the district number</b>, making it easier
		to pinpoint locations within the city.
	</li>
	<li>
		<b>Town entry signs:</b> These feature a <b>blue outline</b> with a <b>white background</b> and <b>black text</b>.
	</li>
	<li>
		<b>Directional signs:</b> Similar to town entry signs, they have a blue outline, white background, and black text.
	</li>
	<li>
		<b>Pedestrian signs:</b> <b>Unique in Europe</b>, Austrian pedestrian signs have <b>two horizontal dashed lines</b>.
	</li>
	<li>
		<b>Chevrons:</b> Austria uses distinctive chevrons with either a <b>white arrow on a red background</b> or a
		<b>red arrow on a yellow background</b>.
	</li>
</ul>

