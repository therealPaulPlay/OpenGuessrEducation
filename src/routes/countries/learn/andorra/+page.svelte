<h2 class="text-2xl font-bold mt-8 mb-2">Language</h2>
<p class="mb-2">
	In Andorra, the main language is <b>Catalan</b>. It is a romance language, pretty similar to Spanish and French. It
	can be tough to distinguish between Catalan and Spanish.
</p>
<ArticleTip>
	Be careful! Catalan is also used in some regions of Spain, such as Catalonia, Valencian Community and the Balearic
	Islands.
</ArticleTip>
<h2 class="text-2xl font-bold mt-8 mb-2">License plate</h2>
<p class="mb-2">
	Andorra is not part of the European Union, so the license plates don't have a blue stripe on the left side.
</p>
