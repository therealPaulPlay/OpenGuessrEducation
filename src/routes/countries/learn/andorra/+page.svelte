<h2 class="text-2xl font-bold mt-8 mb-2">Road numbering system</h2>
<p class="mb-2">Roads in Andorra are divided into two different categories:</p>
<ul class="mb-2 list-disc list-inside">
	<li>
		<b>Main Roads</b>: CG-[1-6]. CG-1 is the main road. It goes from the capital to Spain. Meanwhile, CG-2 goes to
		France. There are small variations called CG-1A and CG-2A located next to these two roads.
	</li>
	<li>
		<b>Secondary roads</b>: CS-[1XX-5XX]. Numbers are derived from CG numbers (e.g. CS-38 is near CG-3).
	</li>
	<ArticleEmbedPanorama src="https://pbs.twimg.com/media/GbP5CmnWMAA1w_1?format=png&name=small"></ArticleEmbedPanorama>
	<p class="text-center mt-2">Main roads in Andorra.</p>
</ul>
<p class="mb-2">
