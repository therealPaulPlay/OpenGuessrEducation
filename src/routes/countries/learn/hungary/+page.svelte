<h2 class="text-2xl font-bold mt-8 mb-2">Strategies</h2>
<ul class="mb-2 list-disc list-inside">
	<li>Look for the unique Hungarian letters <b>ő</b> and <b>ű</b>.</li>
	<li>Many signs contain accented letters such as <b>á, é, í, ó, ö, ú, ü</b></li>
	<li>Hungarian roads often feature concrete utility poles with holes running down their sides.</li>
	<li>White roadside bollards with black and red markings are common.</li>
	<li>Street names frequently contain the word <b>utca</b>.</li>
	<li>Motorways are marked with blue signs and route numbers beginning with <b>M</b>.</li>
</ul>

<h2 class="text-2xl font-bold mt-8 mb-2">Details</h2>
<ul class="mb-2 list-disc list-inside">
	<li>Most of Hungary is relatively flat, especially east of the Danube.</li>
	<li>Vehicles use EU-style license plates with a blue strip on the left.</li>
	<li>Taxis commonly use yellow license plates.</li>
	<li>Agricultural fields cover large portions of the countryside.</li>
	<li>Hungarian is unrelated to most neighboring languages, making text clues very useful.</li>
</ul>

